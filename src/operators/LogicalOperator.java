package operators;

public class LogicalOperator {
    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;

        // Logical AND (&&)
        System.out.println("x && y : " + (x && y));

        // Logical OR (||)
        System.out.println("x || y : " + (x || y));

        // Logical NOT (!)
        System.out.println("!x : " + (!x));
    }
}
